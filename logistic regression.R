make_data <- function(n = 1000, p = 0.5, 
                      mu_0 = 0, mu_1 = 2, 
                      sigma_0 = 1,  sigma_1 = 1){
  
  y <- rbinom(n, 1, p)
  f_0 <- rnorm(n, mu_0, sigma_0)
  f_1 <- rnorm(n, mu_1, sigma_1)
  x <- ifelse(y == 1, f_1, f_0)
  
  test_index <- createDataPartition(y, times = 1, p = 0.5, list = FALSE)
  
  list(train = data.frame(x = x, y = as.factor(y)) %>% slice(-test_index),
       test = data.frame(x = x, y = as.factor(y)) %>% slice(test_index))
}
dat <- make_data()
dat$train %>% ggplot(aes(x, color = y)) + geom_density()


new_make_data <- function(mu_1) {
  dataset<-make_data(mu_1=mu_1)
  fit_glm <- glm(y ~ x, data=dataset$train, family = "binomial")
  p_hat_glm <- predict(fit_glm, dataset$test)
  y_hat_glm <- factor(ifelse(p_hat_glm > 0.5, 1, 0)) # if P>0.5, then 1 else 0.
  confusionMatrix(data = y_hat_glm, reference = dataset$test$y)$overall["Accuracy"]
}
